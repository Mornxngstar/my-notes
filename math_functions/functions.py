import math

pi = 3.14
x = 1
y = 2
z = 3

print(round(pi))  #rounds to the nearest integer number
print(math.ceil(pi))  #rounds up to the nearest integer number
print(math.floor(pi))  #rounds down to the nearest integer number
print(abs(pi))  #gives the absolute value of a number
print(pow(pi,2))  #raises a base number to a power i.e 3.14**2
print(math.sqrt(pi))  #gives the square root of a number
print(max(x, y, z))  #finds the largest value
print(min(x, y, z))  #finds the minimun value
